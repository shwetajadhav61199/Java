class RelationalOperator{
	public static void main(String args[]){
		int a = 5, b = 5;
		System.out.println("a == b "+(a == b));//true
		System.out.println("a != b "+(a != b));//false
		System.out.println("a < b "+(a < b));//false
		System.out.println("a > b "+(a > b));//false
		System.out.println("a <= b "+(a <= b));//true
		System.out.println("a <= b "+(a >= b));//true
	}
}
